export function useT() {
  const language = useAiStore(s => s.language)
  return (key: TKey): string =>
    (DICT[language]?.[key] ?? EN[key]) as string
}

const CHANNEL_TAB_KEYS: Record<string, TKey> = {
  'General': 'tabGeneral', 'Setup': 'tabSetup', 'Delta': 'tabDelta',
  'Ride Height': 'tabRideHeight', 'Rake': 'tabRake',
  'Wheel Speed': 'tabWheelSpeed', 'Wheel Spin': 'tabWheelSpin',
  'Shocks': 'tabShocks', 'Shocks Hist': 'tabShocksHist',
  'Tyre Temp': 'tabTyreTemp', 'Tyre Pressure': 'tabTyrePressure',
}

export function translateChannelLabel(label: string, t: (k: TKey) => string): string {
  const key = CHANNEL_TAB_KEYS[label]
  return key ? t(key) : label
}
